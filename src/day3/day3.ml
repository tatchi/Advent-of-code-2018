module MyMap = Map.Make (struct
  type t = int * int

  let compare = Pervasives.compare
end)

type claim = {id: int; x: int; y: int; width: int; height: int}

let createCoordonates {x; y; width; height} =
  let rec helper acc (dx, dy) =
    match (dx, dy) with
    | x', y' when x' >= width - 1 && y' >= height -> acc
    | x', y' when y' < height -> helper ((x + x', y + y') :: acc) (x', dy + 1)
    | x', y' when y' >= height -> helper acc (dx + 1, 0)
    | _, _ -> acc
  in
  helper [] (0, 0)

exception MalFormedInput

let createClaim s =
  let atIndex = String.index s '@' in
  let doublePointIndex = String.index s ':' in
  let id = String.sub s 1 (atIndex - 2) in
  let coordinates =
    String.sub s (atIndex + 2) (doublePointIndex - (atIndex + 2))
  in
  let size =
    String.sub s (doublePointIndex + 2)
      (String.length s - (doublePointIndex + 2))
  in
  let x, y =
    match String.split_on_char ',' coordinates with
    | [x; y] -> (x, y)
    | _ -> raise MalFormedInput
  in
  let width, height =
    match String.split_on_char 'x' size with
    | [width; height] -> (width, height)
    | _ -> raise MalFormedInput
  in
  { id= int_of_string id
  ; x= int_of_string x
  ; y= int_of_string y
  ; width= int_of_string width
  ; height= int_of_string height }

