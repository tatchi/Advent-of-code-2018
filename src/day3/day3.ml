let noOverlap c1 c2 =
  c1.x + c1.width <= c2.x
  || c1.x >= c2.x + c2.width
  || c1.y + c1.height <= c2.y
  || c1.y >= c2.y + c2.height;;

let getNoOverlap claims =
  List.fold_left
    (fun acc baseClaim ->
      match acc with
      | Some x -> Some x
      | None -> (
        match
          List.for_all
            (fun claimToCompare ->
              claimToCompare = baseClaim || noOverlap baseClaim claimToCompare
              )
            claims
        with
        | true -> Some baseClaim.id
        | false -> None ) )
    None claims

